const generateFloor = (prefix, sold) =>
  Array.from({ length: 16 }, (_, index) => {
    const id = `${prefix}${index + 1}`
    return {
      id,
      label: id,
      status: sold.includes(index) ? 'sold' : 'empty',
    }
  })

